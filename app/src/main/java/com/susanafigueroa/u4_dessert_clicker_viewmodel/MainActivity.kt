                Surface(
                    modifier = Modifier
                        .fillMaxSize()
                        .statusBarsPadding(),
                ) {
                    DessertClickerApp(
                        viewModel = DessertClickerViewModel(),
                        desserts = Datasource.dessertList
                    )
                }
            }
        }
